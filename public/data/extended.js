function getXP() {
  return parseInt(localStorage.getItem('upsc_xp') || '0');
}

function addXP(amount, reason) {
  const current = getXP();
  const newXP = current + amount;
  localStorage.setItem('upsc_xp', newXP);
  return { old: current, new: newXP, gained: amount, reason };
}
